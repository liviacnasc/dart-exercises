void visitorCount(){
  for(int i = 0; i < 10; i++){
    print(i);
  }
}

void productCount(){
  for(int i = 0; i < 10; i++){
    if(i % 2 == 0){
      print(i);
    }
  }
}

void countdown(){
  for(int i = 9; i >= 0; i--){
    print(i);
  }
}

void squareCount(){
  for(int i = 1; i < 11; i++){
    print(i * i);
  }
}